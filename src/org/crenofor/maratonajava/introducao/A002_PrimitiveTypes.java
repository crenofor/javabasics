package org.crenofor.maratonajava.introducao;

public class A002_PrimitiveTypes {
    public static void main(String[] args) {
        //int, double, float, char, byte, short, long, boolean
        int age = 10;
        System.out.println("A idade é "+age);
        double buxa = 0.7;
        System.out.println(buxa);

        //String não é um tipo primitivo e sim uma class, e um 'tipo de referência'
        String nome = "text";
        System.out.println(nome);
    }
}
